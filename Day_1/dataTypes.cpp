#include <iostream>
using namespace std;

int main() {
    // Boolean
    bool boolean = true;
    cout << "Boolean: " << boolean << endl;

    // Character
    char character = 'A';
    cout << "Character: " << character << endl;

    // Integer
    int integer = 10;
    cout << "Integer: " << integer << endl;

    // Floating Point
    float floatPoint = 10.5;
    cout << "Float Point: " << floatPoint << endl;

    // Double
    double doublePoint = 10.5;
    cout << "Double: " << doublePoint << endl;

    // Signed and Unsigned Integers
    signed int signedInteger = 10;
    unsigned int unsignedInteger = 10;
    cout << "Signed Integer: " << signedInteger << endl;
    cout << "Unsigned Integer: " << unsignedInteger << endl;

    // Signed and Unsigned Long Integers
    signed long long signedLongLong = 10;
    unsigned long long unsignedLongLong = 10;
    cout << "Signed Long Long: " << signedLongLong << endl;
    cout << "Unsigned Long Long: " << unsignedLongLong << endl;


    return 0;
}